                        <span class="dropcap rounded">N</span>ow that you’ve got your tattoo, it’s up to you to ensure that it’s given the best opportunity to heal. Often, tattoos will appear fully healed at around the two-week mark. However, the healing process itself occurs in three stages, which can take up to six weeks (always be aware that there is more to healing than simply what you can see on the surface). It’s necessary you are following proper aftercare procedure, using  <strong>only</strong> the products that are recommended to you by your artist.
                    </p>

                   <div class="row mt50">
                        <h3 class="gold-text">Stage 1: Days 1-6</h3>
                    </div>

                    <p class="mt15">
                        Despite being a beautiful work of art, your new tattoo is also now an open wound that your body aims to heal. In the beginning, your tattoo will weep blood, plasma, lymphatic fluid, and ink (all of which are normal). The amount of this fluid mixture left on the skin should always be kept to minimum as this can eventually lead to scabbing if left to dry.
                    </p>
                    <p class="mt15">
                        Please keep in mind that you should <strong>never</strong> pick off anything that does not come off naturally with a gentle cleanse.
                        You may also experience mild swelling and/or redness around the tattoo, which results from an increase in blood flow to the area as the body’s natural immune response kicks in.
                    </p>
                    <p class="mt25">
                        <ul>
                            <li>
                                Keep your tattoo covered for the first 24-30 hours following your appointment (with Rocky Mountain Tattoo provided dressings only). Only remove your dressing once you are home and/or in a clean environment.
                            </li>
                            <li>
                                <strong>Never</strong> touch your tattoo without first washing your hands. We recommend washing in accordance with the AHS hand-hygiene procedure:
                                <ol style="list-style-type: decimal;" class="mt15 gold-text">
                                    <li>
                                        Wet hands with warm water and apply enough soap to ensure all hand surfaces are lathered thoroughly (i.e. two or three pumps).
                                    </li>
                                    <li>
                                        Vigorously rub soap product over all the surfaces of the hands and wrists, including: palms, spaces between fingers, back of hands and wrists, fingers, fingertips, and thumbs for a <strong>minimum</strong> of 15 seconds. Don’t forget to check under your nails.
                                    </li>
                                    <li>
                                        Rinse hands under warm running water (allowing the water to flow downwards from wrists to fingertips).
                                    </li>
                                    <li>
                                       Pat hands dry with a clean, disposable paper towel (<strong>do not</strong> use a multi-use hand towel).
                                    </li>
                                    <li>
                                        Use paper towel to turn off faucets and discard.
                                    </li>
                                </ol>
                            </li>
                            <li>
                                Using a mild unscented soap and clean warm water, <strong>gently</strong> wash off all plasma and excess ink using the pads of your fingers in a circular motion.
                            </li>
                            <li>
                                <strong>Gently</strong> apply your aftercare product, ensuring that you rub it all the way into your skin. It is best to use a clean tool (like a single use disposable tongue depressor) to scoop out your product if it's not in a squeezable container.  This eliminates the chances of contaminating your product with any residual bacteria that may be on your hands.
                            </li>
                            <li>
                                <p>If you've purchased a second transparent dressing (recommended), apply this now (with help as necessary), ensuring that whoever helps you has cleaned their hands too. This dressing may stay in place for no longer than five days. If at any time the edges of the dressing become lifted or dirtied, or if any part of the tattoo becomes exposed within the five day period, remove the dressing immediately to prevent any trapped dirt/bacteria from being in prolonged contact with your tattoo. When removing this dressing, gently peel an edge downward, or stretch neighboring edges until the dressing lifts off of the skin. If you are having trouble removing your dressing, hop into the shower and allow the water to run over the top of the dressing as you gently peel it off.</p>
                                <p>
                                    If you haven’t purchased a second dressing, gently cleanse and moisturize your tattoo at least 2-3 times daily (more often of you are producing larger volumes of fluid)
                                </p>
                            </li>
                            <li>
                                Avoid soaking your tattoos, or participating in activities that might create a prolonged moist skin surface (for at least 2 weeks). Avoid baths, hot tubs, pools, lakes, saunas, sporting activities, and weight rooms. Any environment with moist air should be avoided.
                            </li>
                            <li>
                                Avoid tight-fitting or irritating clothing that may rub over and damage your tattoo. If this is unavoidable, please discuss this with your artist prior to getting tattooed.
                            </li>
                            <li>
                                Ensure that your bed linens are washed on the hottest setting, daily, for the first couple of days (Depends on aftercare bandage used). Do your best not to sleep or lie on your tattoo. If you wake up with your bedding stuck to the tattoo simply take it into the shower with you and soak it until it detaches itself.
                            </li>
                            <li>
                               Avoid letting your tattoo touch surfaces that you cannot ensure are free from dirt or bacteria (this includes previously worn clothing). It’s also best not to let others touch your tattoo at all during the healing process (remember, they may carry bacteria that could cause an infection if introduced into your tattoo).
                            </li>
                            <li>
                                Keep your tattoo away from pets or animals (especially cats, birds, reptiles, and livestock), and be wary of surfaces they may have touched. Because of the prevalence of pet/animal ownership, Rocky Mountain Tattoo Ltd. refers to AHS Infection, Prevention, and Control (IPC) resources to ensure that its animal-loving clients be made aware of some of the potential risks that exposure to certain animals and/or animal environments may put them in during the healing process. We feel that this is vital because it stresses the importance of strict post-tattoo hygiene, and ensures that our clients can make informed decisions about getting tattooed

                            </li>
                            <li>
                                <p>
                                    <strong>Ensure to moisturize.</strong> Once uncovered, apply a thin layer of your aftercare at least twice daily to prevent excess drying or scabbing. However, there is such a thing as too much moisture. Absorption of too much of the active ingredient could potentially cause pimpling.
                                </p>
                                <p>
                                   After the first couple of days, you may begin to form small scabs or dried flakes on top of your tattoo giving it a faded appearance. This is normal, and it will now be more important than ever to ensure that you are applying your aftercare regularly. It's not normal to develop very large or deep scabbing, open areas of skin, redness, swelling, a sensation of warmth that seems to be getting worse, yellow/green oozing (i.e. pus), large raised bumps, and a tattoo that just doesn’t seem to be healing. This can happen for any number of reasons.
                                </p>
                                <p>
                                    Excess scabbing can occur due to the depth and amount of ink that is packed into one area of the skin to achieve darker shading, or, an individual’s reaction to the ink (rare, but possible). Large raised bumps (also known as a red-ink-reaction) can occur in those with extremely sensitive skin or underlying sensitivities to red dyes, in some cases causing unsightly keloid scarring. Lastly, for tattoos that do not seem to be progressing through the expected stages of healing, there may be an underlying factor causing impairment in the wound healing process. This last point is why it is imperative that your artist is aware of any applicable known medical conditions (listed on our consent form) before starting the tattoo.
                                </p>
                                <p>
                                    As a side note, we have found that those who opt to use the second transparent dressing experience little to no scabbing, considerably less flaking, and markedly faster healing times than those who do not. We will always recommend this option. Additional dressings may be purchased from us anytime during regular business hours.

                                </p>
                            </li>
                        </ul>
                    </p>

                   <div class="row mt50">
                        <h3 class="gold-text">Stage 2: Days 7-14</h3>
                    </div>

                    <p class="mt15">
                        During this stage, your tattoo will likely begin to look very dry and dull, and will begin to flake off bits of skin. To many, it’s also the most difficult stage due to the accompanying itch. There are a few very important things to remember during this stage
                    </p>

                    <p class="mt25">
                        <ul>
                            <li>
                               <strong>Never</strong> rub or scratch any part of your tattoo. If you are experiencing any itchiness, lightly pat or tap your tattoo instead.
                            </li>
                            <li>
                                The more stringently that you use your aftercare, the less flaking and itching you will experience. The same goes for scabbing&mdash;it is best to prevent scabs from forming in the first place and the best way to do this is to keep the tattoo moist.
                            </li>
                            <li>
                                If you do form scabs, leave them be. It is imperative that you never pick off any dried bits of skin or scabbed areas, even if they seem to be hanging on by a thread. Not only could this delay healing, but it could also produce scarring. Your body will then need to produce an additional protective layer, causing you to lose your ink a second time. As a result, you will be left with faded areas or gaps in your tattoo, which will be recognizable to your artist. The best thing to do in this case would be to cleanse the tattoo using the same technique described in stage 1 (to allow the dried bits to wash off naturally), and pat dry the skin as gently and completely as possible. If your skin is still wet when applying your aftercare, the water trapped between your skin and the lotion could then be absorbed by the scab, causing it to become soft and more susceptible to being pulled off accidentally. Take care that none of the scabs have adhered to the paper towel before lifting it off, and then apply your aftercare product
                            </li>
                            <li>
                                Keep in mind that your tattoo won’t look pretty during this stage; but don’t worry, it’s all part of the process!
                            </li>
                        </ul>
                    </p>

                   <div class="row mt50">
                        <h3 class="gold-text">Stage 3: Days 15+ (Can last up to 6 weeks)</h3>
                    </div>

                    <p class="mt15">
                        During this final stage of healing, your tattoo may still look slightly dull and continue to have some fine flaky bits until the fresh new layer of skin is exposed. Your skin can also look slightly shiny, but this will gradually improve over the next few weeks until your tattoo looks much clearer, brighter, and sharper&mdash;and your skin texture is back to normal. It will be later on in this stage that you will be able to tell if any touch-up work is needed. A couple of things to remember:
                    <p class="mt25">
                        <ul>
                            <li>No picking!</li>
                            <li>Continue to protect your tattoo from direct sun or uv light, as well as harsh chemicals</li>
                            <li>Continue to moisturize even if it’s shiny</li>
                        </ul>
                    </p>

